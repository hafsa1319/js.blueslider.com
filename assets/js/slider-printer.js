let localCssSlider = "https://js.blueslider.com/assets/css/blueslider.css";
let localSlider = "https://js.blueslider.com/assets/js/blueslider.js";
let newRunSlider = document.createElement("script");
let newCssSlider = document.createElement("link");
let swiperCssSlider = document.createElement("link");
let swiperJsSlider = document.createElement("script");
swiperCssSlider.rel = "stylesheet";
swiperCssSlider.href = "https://cdn.jsdelivr.net/npm/swiper@8/swiper-bundle.min.css";
swiperJsSlider.src = "https://cdn.jsdelivr.net/npm/swiper@8/swiper-bundle.min.js";
newCssSlider.rel = "stylesheet";
newCssSlider.href = `${localCssSlider}?v=${new Date().getTime()}`;
newRunSlider.src = `${localSlider}?v=${new Date().getTime()}`;
newRunSlider.type = "module";
document.body.append(swiperJsSlider);
